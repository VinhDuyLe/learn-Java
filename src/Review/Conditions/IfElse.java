package Review.Conditions;

public class IfElse {
    public static void main(String[] args) {
        int x = 10, y = 30;
        if (x == 20) {
            if (y == 40) {
                System.out.println("a");
            }else {
                System.out.println("b");
            }
        }else {
            System.out.println("c");
        }
    }
}
