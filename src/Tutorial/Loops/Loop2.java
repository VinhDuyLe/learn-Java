package Tutorial.Loops;

public class Loop2 {
    public static void main(String[] args) {
        int [] numbers = {5 ,4 ,8, 8, 10};

        for (int x : numbers) {
            System.out.println(x);
        }

        String [] lists = {"A", "B", "C", "E"};
        for (String y : lists) {
            System.out.println(y);
        }
    }
}
